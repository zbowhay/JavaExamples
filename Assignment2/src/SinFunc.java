/**
 * SinFunc Class extends Function and is used for finding the roots of f(x) = Sin(x)
 * @author Zac R Bowhay
 *	Created September 16, 2012
 */
public class SinFunc extends Function{
	/**
	 * Evaluate
	 * @param x is the double entered that will be evaluated by Sin(x)
	 * @return returns the sin(x)
	 */
	public double evaluate(double x){
		return Math.sin(x);
	}
}
