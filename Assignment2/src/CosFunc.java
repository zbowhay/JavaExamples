/**
 * CosFunc Class extends Function and is used for finding the roots of f(x) = Cos(x)
 * @author Zac R Bowhay
 *	Created September 16, 2012
 */
public class CosFunc extends Function{
	/**
	 * Evaluate
	 * @param x is the double entered that will be evaluated by Cos(x)
	 * @return returns the cos(x)
	 */
	public double evaluate(double x){
		return Math.cos(x);
	}
}
